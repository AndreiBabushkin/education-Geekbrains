"""Условия задачи: Определить, какое число в массиве встречается чаще всего."""

list_1 = [random.randint(1, 5) for i in range(15)]
print(list_1)

num = 0
count = 0

for i in list_1:
    q = list_1.count(i)
    if q > count:
        count = q
        num = i
print(f'{count} раза встречается число {num}')

size_t_3 = sys.getsizeof(list_1) + sys.getsizeof(num) + sys.getsizeof(count) + sys.getsizeof(q)
print(f'Количество затраченной памяти - {size_t_3} байт')

